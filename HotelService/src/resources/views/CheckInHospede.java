    private void TableServicosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableServicosMouseClicked
        int linha = TableServicos.getSelectedRow();
        if (linha >= 0) {
            int id = Integer.valueOf(TableServicos.getValueAt(TableServicos.getSelectedRow(), 0).toString());
            this.resultSet = this.servicoController.show(id);
            if (this.resultSet != null) {
                try {
                    cleanInputsPanel2();
                    this.TituloServico.setText(this.resultSet.getString("nome"));

                    this.DescricaoServico.setText("");
                    StyledDocument doc = this.DescricaoServico.getStyledDocument();
                    doc.insertString(0, "DETALHES DO SERVIÇO:\n", null);
                    doc.insertString(doc.getLength(), "Descrição:\n" + this.resultSet.getString("descricao") + "\n", null);
                    doc.insertString(doc.getLength(), "Valor: " + Helper.formatPrice(Double.valueOf(this.resultSet.getString("preco"))) + "\n", null);
                    doc.insertString(doc.getLength(), "Qtde Crianças: " + this.resultSet.getString("qtde_crianca_max") + "\n", null);
                    doc.insertString(doc.getLength(), "Qtde Pessoas: " + this.resultSet.getString("qtde_pessoa_max") + "\n", null);
                    doc.insertString(doc.getLength(), "Vagas: " + this.resultSet.getString("vagas") + "\n", null);

                } catch (SQLException ex) {
                    System.out.println("Erro na função TableServicosMouseClicked(): " + ex);
                } catch (BadLocationException ex) {
                    System.out.println("Erro na class StyledDocument in TableServicosMouseClicked(): " + ex);
                }
            }
        }
    }//GEN-LAST:event_TableServicosMouseClicked

