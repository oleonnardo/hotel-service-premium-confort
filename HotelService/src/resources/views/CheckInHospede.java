    private void btnRemoveServicoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRemoveServicoMouseClicked
        int linha = TableCompraServicos.getSelectedRow();
        if (linha >= 0) {
            int id = Integer.valueOf(TableCompraServicos.getValueAt(linha, 0).toString());
            this.servicos.reporVaga(Integer.valueOf(this.carrinhoDeCompras.get(id)[1]));
            this.carrinhoDeCompras.remove(id);
            loadTableCompraServico();
            loadTableServicos(this.servicoController.index());
        } else {
            Flash.error("Selecione um serviço do carrinho.");
        }
    }//GEN-LAST:event_btnRemoveServicoMouseClicked

